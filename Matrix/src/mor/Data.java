package mor;

public class Data {
    int x;
    int y;
    int data;

    public Data(int x, int y, int data) {
        this.x = x;
        this.y = y;
        this.data = data;
    }


    public int getX() {
        return x;
    }


    public int getY() {
        return y;
    }

    public void setData(int data) {
        this.data = data;
    }

    public int getData() {
        return data;
    }
}

